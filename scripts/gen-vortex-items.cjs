/**
 * Generates lib/vortex-items.json (300+ items, English max 3 words).
 * Run: node scripts/gen-vortex-items.cjs
 */
const fs = require("fs");
const path = require("path");

const items = [];

function maxWords(s) {
  return s.trim().split(/\s+/).filter(Boolean).length;
}

function add(spanish, english, difficulty, type, category, acceptedEn, note) {
  if (maxWords(english) > 3) throw new Error(`English >3 words: "${english}"`);
  if (maxWords(spanish) > 6) throw new Error(`Spanish >6 words: "${spanish}"`);
  for (const a of acceptedEn ?? []) {
    if (maxWords(a) > 3) throw new Error(`acceptedEn >3 words: "${a}"`);
  }
  const row = { spanish, english, difficulty, type, category: category || undefined };
  if (acceptedEn?.length) row.acceptedEn = acceptedEn;
  if (note) row.note = note;
  items.push(row);
}

function bulk(rows, difficulty, type, category) {
  for (const r of rows) {
    if (Array.isArray(r)) add(r[0], r[1], difficulty, type, category || r[2], r[3], r[4]);
    else add(r.es, r.en, difficulty, type, r.cat, r.a, r.n);
  }
}

